    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.setCharacterEncoding("UTF-8");
        String id = request.getParameter("id").trim();
        String name = request.getParameter("name").trim();
        String catID = request.getParameter("catID").trim();
        String img = "images/CMC/".concat(request.getParameter("img").trim());
        int price = Integer.parseInt(request.getParameter("price").trim());
        int quantity = Integer.parseInt(request.getParameter("quantity").trim());
        String describe = request.getParameter("describe").trim();
        String status = request.getParameter("status").trim();
        String date = request.getParameter("date").trim();
        ProductDB pdb = new ProductDB();
        if(!pdb.existedPro(id)){
            Product p = new Product(id, name, catID, img, price, quantity, describe, status, date);
            pdb.insert(p);
            response.sendRedirect("homeAdmin.jsp");
        }else{
            String ms = "existed" + id;
            request.setAttribute("error", ms);
            RequestDispatcher rs = request.getRequestDispatcher("addNewPro.jsp");
            rs.forward(request, response);
        }
        
    }

